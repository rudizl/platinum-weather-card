// Resolve language code to nearest supported translation
export function resolveLanguage(lang: string): string {
  const base = lang.split('-')[0].toLowerCase();
  if (translations[lang]) return lang;      // exact match (e.g. 'bg-BG')
  if (translations[base]) return base;      // base match (e.g. 'bg')
  return 'en';                              // fallback
}
